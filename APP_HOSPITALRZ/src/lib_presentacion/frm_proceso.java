    private dprincipal aniadir(Integer id, boolean cbx, Integer estado) {
        dprincipal dprin = new dprincipal();
        dieta die = new dieta();
        die.setId(id);
        dprin.setIddieta(die);
        dprin.setActivado(cbx);
        dprin.setIdestado(estado);
        return dprin;
    }

